# Get the absolute value of a number
print(abs(-10))

# Convert an integer to a binary string
print(bin(10))

# Create a complex number
print(complex(1, 2))

# Get the quotient and remainder of a division operation
print(divmod(10, 3))

# Convert a number to a floating-point number
print(float(10))

# Convert a number to an integer
print(int(10.5))

# Get the largest number in a sequence
print(max(1, 2, 3))

# Get the smallest number in a sequence
print(min(1, 2, 3))

# Raise a number to a power
print(pow(2, 3))

# Round a number to a specified number of decimal places
print(round(10.5, 2))